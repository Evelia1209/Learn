#循环输出0-9
for i in range(10):
    print(i)

#循环输出1-10的两种方法
for i in range(1,11):
    print(i)
for i in range(10):
    print(i)

#反向输出
for i in range(10,0,-1):
    print(i)
for i in range(10):
    print(10-i)